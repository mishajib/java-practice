
public class Datatypes {

    public static void main(String[] args) {
        // Primitive data-types
        byte age = 25;
        System.out.println("Age is: " + age);

        short salary = 20000;
        System.out.println("Salary is: " + salary);

        int population = 160000000;
        System.out.println("Population: " + population);

        long worldDebt = 9000000000000000000L;
        System.out.println("World Debt: " + worldDebt);
        float average = 75.55f;
        System.out.println("Average Marks: " + average);

        double bankBalance = 123456789.123456;
        System.out.println("Bank Balance: " + bankBalance);
        boolean isLoggedIn = true;
        System.out.println("Is user logged in? " + isLoggedIn);
        char grade = 'A';
        System.out.println("Grade: " + grade);

    }
}
